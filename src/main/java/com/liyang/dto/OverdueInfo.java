package com.liyang.dto;

import java.math.BigDecimal;
import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

public class OverdueInfo {
	
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	private Date createAt;  //逾期信息创建时间
	
	@JsonFormat(pattern = "yyyy-MM-dd HH:mm:ss",timezone = "GMT+8")
	private Date finishAt;  //逾期信息结清时间
	 
	private BigDecimal historyAmount; //历史逾期金额
	
	private BigDecimal overdueAmount; // 当前逾期金额
	
	private Integer overdueDays;   //逾期天数
	
	private BigDecimal penalSum;  // 罚金
	
	private Integer status;  //逾期状态
	
	private String name;  //法人姓名
	
	private BigDecimal principal;  //借款本金
	
	private String  phone;  //法人手机号
	
	private Integer loanId; //借款ID
	
	private String orderNo;  //借款单号
	
	private Integer overdueId;//逾期ID
	
	
	
	

	public Integer getOverdueId() {
		return overdueId;
	}

	public void setOverdueId(Integer overdueId) {
		this.overdueId = overdueId;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public Date getCreateAt() {
		return createAt;
	}

	public void setCreateAt(Date createAt) {
		this.createAt = createAt;
	}

	public Date getFinishAt() {
		return finishAt;
	}

	public void setFinishAt(Date finishAt) {
		this.finishAt = finishAt;
	}

	public BigDecimal getHistoryAmount() {
		return historyAmount;
	}

	public void setHistoryAmount(BigDecimal historyAmount) {
		this.historyAmount = historyAmount;
	}

	public BigDecimal getOverdueAmount() {
		return overdueAmount;
	}

	public void setOverdueAmount(BigDecimal overdueAmount) {
		this.overdueAmount = overdueAmount;
	}

	public Integer getOverdueDays() {
		return overdueDays;
	}

	public void setOverdueDays(Integer overdueDays) {
		this.overdueDays = overdueDays;
	}

	public BigDecimal getPenalSum() {
		return penalSum;
	}

	public void setPenalSum(BigDecimal penalSum) {
		this.penalSum = penalSum;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BigDecimal getPrincipal() {
		return principal;
	}

	public void setPrincipal(BigDecimal principal) {
		this.principal = principal;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Integer getLoanId() {
		return loanId;
	}

	public void setLoanId(Integer loanId) {
		this.loanId = loanId;
	}
	
	

}
