package com.liyang.dto;

import java.math.BigDecimal;

public class CreditcardInfo {
	private Integer id;               //信用卡ID

	private BigDecimal creditBalance; // 可用授信额度  借款余额
	
	private BigDecimal creditLoan;    // 当前借款总额度
	
	private BigDecimal creditGrant;   // 授信总额度
	
	private String telephone;         // 客户联系方式
	
	private String name;              // 客户姓名
	
	private String productLabel;      // 产品名称
	
	private String branchesName;      // 网点名称
	
	private String agentBrand;        // 代理品牌
	
	private String businessArea;      // 经营区域
	
	private Integer applyDayPickExpress; // 日收件量
	
	private Integer applyDayPatchExpress; // 日派件量
	
	private String serviceName;          //业务员
	
	private double margin;           //保证金
	
	private String identity;         //身份证
	
	

	public String getIdentity() {
		return identity;
	}

	public void setIdentity(String identity) {
		this.identity = identity;
	}

	public double getMargin() {
		return margin;
	}

	public void setMargin(double margin) {
		this.margin = margin;
	}

	public String getServiceName() {
		return serviceName;
	}

	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public BigDecimal getCreditBalance() {
		return creditBalance;
	}

	public void setCreditBalance(BigDecimal creditBalance) {
		this.creditBalance = creditBalance;
	}

	public BigDecimal getCreditLoan() {
		return creditLoan;
	}

	public void setCreditLoan(BigDecimal creditLoan) {
		this.creditLoan = creditLoan;
	}

	public BigDecimal getCreditGrant() {
		return creditGrant;
	}

	public void setCreditGrant(BigDecimal creditGrant) {
		this.creditGrant = creditGrant;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProductLabel() {
		return productLabel;
	}

	public void setProductLabel(String productLabel) {
		this.productLabel = productLabel;
	}

	public String getBranchesName() {
		return branchesName;
	}

	public void setBranchesName(String branchesName) {
		this.branchesName = branchesName;
	}

	public String getAgentBrand() {
		return agentBrand;
	}

	public void setAgentBrand(String agentBrand) {
		this.agentBrand = agentBrand;
	}

	public String getBusinessArea() {
		return businessArea;
	}

	public void setBusinessArea(String businessArea) {
		this.businessArea = businessArea;
	}

	public Integer getApplyDayPickExpress() {
		return applyDayPickExpress;
	}

	public void setApplyDayPickExpress(Integer applyDayPickExpress) {
		this.applyDayPickExpress = applyDayPickExpress;
	}

	public Integer getApplyDayPatchExpress() {
		return applyDayPatchExpress;
	}

	public void setApplyDayPatchExpress(Integer applyDayPatchExpress) {
		this.applyDayPatchExpress = applyDayPatchExpress;
	}
	
	
}
